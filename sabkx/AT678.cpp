struct Image{
    vector<vector<int> > vec;
    int h,w;
    Image(){
        vec.clear();
        h=w;
    }
    Image(int x,int y){
        for(int i=0;i<x;i++){
            vector<int> temp;
            temp.clear();
            for(int j=0;j<y;j++){
                temp.push_back(0);
            }
            vec.push_back(temp);
        }
        h=x;
        w=y;
    }
    void build(vector<string> input){
        vec.clear();
        vector<int> temp;
        for(string str:input){
            for(char i:str){
                if(i=='#'){
                    temp.emplace_back(1);
                }else{
                    temp.emplace_back(0);
                }
            }
            vec.push_back(temp);
            temp.clear();
        }
    }
    void print(){
        for(int i=0;i<vec.size();i++){
            for(int j=0;j<vec[i].size();j++){
                if(vec[i][j]==1){
                    printf("#");
                }else{
                    printf(".");
                }
            }
            printf("\n");
        }
    }
};
inline void copy(Image &to_be_copied,Image to_copy){
    int x_size=to_copy.vec.size();
    int y_size=to_copy.vec[0].size();
    to_be_copied.vec.clear();
    for(int i=0;i<x_size;i++){
        vector<int> temp;
        for(int j=0;j<y_size;j++){
            temp.push_back(to_copy.vec[i][j]);
        }
        to_be_copied.vec.push_back(temp);
    }
}
